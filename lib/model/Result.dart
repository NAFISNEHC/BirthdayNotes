import 'package:flutter_app/model/VerifyCode.dart';

class Autogenerated<T> {
  int code;
  String msg;
  VerifyCOde result;

  Autogenerated({this.code, this.msg, this.result});

  Autogenerated.fromJson(Map<String, dynamic> json) {
    code = json['code'];
    msg = json['msg'];
    result =
        json['result'] != null ? new VerifyCOde.fromJson(json['result']) : null;
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    data['code'] = this.code;
    data['msg'] = this.msg;
    if (this.result != null) {
      data['result'] = this.result.toJson();
    }
    return data;
  }
}
